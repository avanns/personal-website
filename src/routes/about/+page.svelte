	<h1>About Aubin Vanns</h1>

	<p>
		Guitarist, composer and improviser Aubin Vanns has gained a reputation for his mature and original improvisational style as well as his melodic compositions.
	</p>
		In 2018, he was featured as a soloist and accompanist on recordings by Matt Anderson, Jana and the Lanterns and Tom Neale.
	<p>
		From 2013 to 2017 Aubin was half of the Swedish folk jazz duo, Blue Ejder. He mixed folk finger-picking patterns and alternate tunings with contemporary European jazz influences. Their first album featured trumpeter Neil Yates and they performed at numerous venues across the UK and Sweden.
	</p>
	<p>
		Aubin is a graduate of the Royal Academy of Music and Leeds College of Music.
	</p>

